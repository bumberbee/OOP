package phan02;

public class PrePostIncrementDemo {
public static void main(String[] args) {
	// thao tac voi pre-increment va post-increment
			int i =3;
			i++;			//post-increment tang i sau khi sử dụng trong biểu thức mới		
			System.out.println(i);
			++i;			//pre-increment tăng i trước khi sử dụng trong biểu thức mới
			System.out.println(i);
			System.out.println(++i);
			System.out.println(i++);
			System.out.println(--i);
			System.out.println(i--);
			System.out.println(i);
			
}
}
