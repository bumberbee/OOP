package phan02;

public class UnaryDemo {
public static void main(String[] args) {
	// thao tac voi toan tu 1 ngoi
	
			int result02 = +1;
			System.out.println(result02);
			
			result02--;
			System.out.println(result02);
			
			result02 ++;
			System.out.println(result02);
			
			result02 = -result02;
			System.out.println(result02);
			
			boolean success = false;
			System.out.println(success);
			System.out.println(!success);

}
}
